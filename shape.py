def extract_spectral_key(ca_blocks):
    """Extract private key from CA rules via prime-weighted resonance"""
    # Prime resonance weights (as used in vortex)
    primes = [2, 3, 5, 7, 17, 101]
    prime_product = 2*3*5*7*17*101
    
    # Process each block
    block_resonances = []
    for block in ca_blocks:
        # Flatten block to byte array
        byte_stream = bytearray()
        for rule in block:
            for value in rule:
                byte_stream.append(value & 0xFF)
        
        # Compute spectral signature
        h = hashlib.sha256(byte_stream).digest()
        resonance_val = int.from_bytes(h[:4], 'big')  # 32-bit resonance
        
        # Prime-weight the resonance
        weighted_res = (resonance_val * prime_product) % (2**32)
        block_resonances.append(weighted_res)
    
    # Combine all block resonances
    master_resonance = 1
    for res in block_resonances:
        master_resonance = (master_resonance * res) % n
    
    return master_resonance

